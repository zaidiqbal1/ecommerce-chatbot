import json
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# Load dataset
with open("ecommerce_faq.json", "r") as f:
    data = json.load(f)

# Extract questions and answers
questions = [item["question"] for item in data]
answers = [item["answer"] for item in data]

# Vectorize questions
vectorizer = TfidfVectorizer()
X = vectorizer.fit_transform(questions)

print("ðŸ›ï¸ Welcome to the E-commerce Chatbot (CLI Version)")
print("Type 'exit' to quit.\n")

while True:
    query = input("You: ")

    if query.lower() == "exit":
        print("Chatbot: Thank you for visiting! Have a great day! ðŸ‘‹")
        break

    # Transform user query
    query_vec = vectorizer.transform([query])
    similarity = cosine_similarity(query_vec, X).flatten()

    # Get best match
    idx = similarity.argmax()
    score = similarity[idx]

    # Threshold for fallback
    if score > 0.3:
        print("Chatbot:", answers[idx])
    else:
        print("Chatbot: Sorry, I don't have an answer for that. Please contact our customer support. ðŸ“ž")

